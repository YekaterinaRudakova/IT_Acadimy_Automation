package by.it.task_1;

public class Calculator {

    public double add(double firstDouble, double secondDouble) {
        return firstDouble + secondDouble;
    }

    public double sub(double firstDouble, double secondDouble) {
        return firstDouble - secondDouble;
    }

    public double mul(double firstDouble, double secondDouble) {
        return firstDouble * secondDouble;
    }

    public  double div(double firstDouble, double secondDouble) {
        return firstDouble / secondDouble;
    }
}
